<h5>Le CASDAR OTOVEIL en quelques mots…</h5>
<p>
  Le projet CASDAR OTOVEIL est en lien avec le programme Ambition Bio 2017 qui soutient la recherche et le développement pour l’AB et la production d’outils de conseil adaptés à la bio. En effet, OTOVEIL vise à renforcer la détection précoce des ruptures d’équilibre sanitaire des troupeaux de ruminants. Cela dans le but de limiter le recours aux intrants de synthèse (dont les traitements antibiotiques et certains antiparasitaires) et de renforcer les pratiques sanitaires d’élevage plus respectueuses du bien-être animal et de l’environnement, dans une approche de santé intégrée.
</p>
<p>
  L’équilibre de santé d’un troupeau est un concept évoqué dans les fermes biologiques. Il s’agit d’un état global de bonne santé du troupeau, avec peu d’animaux malades et recevant peu d’intrants médicamenteux. Cependant, ce concept n’est pas formalisé; flou, il induit diverses interprétations subjectives. Les travaux conduits dans ce CASDAR avaient donc pour finalité d’objectiver, par des méthodes statistiques adaptées, la notion d’équilibre sanitaire à partir de données enregistrées sur des troupeaux menés en agriculture biologique.
</p>
<p>
  Des méthodes pour la prévention et la surveillance des troupeaux, adaptées à l’AB et transposables en élevage conventionnel, étaient attendues à la fin du projet. C’est ce que nous présentons sous la forme de 4 livrets, un pour chaque principale espèce : bovins lait, bovins viande, ovins lait et viande et caprins.
</p>

